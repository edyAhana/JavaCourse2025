/**
 * Демонстрация работы ключевого слова super при наследовании,
 * когда поле и метод определены только в суперклассе A.
 *
 * Здесь класс C наследует B, а B наследует A. Поле 'a' и метод 'method()'
 * отсутствуют в классе B, поэтому super обращается к членам класса A.
 */
public class SuperAccessExample {

    /**
     * Суперкласс A с полем и методом.
     */
    static class A {
        int a = 10;

        /**
         * Метод суперкласса.
         */
        void method() {
            System.out.println("Метод класса A");
        }
    }

    /**
     * Класс B наследует A, но не определяет свои поля и методы.
     */
    static class B extends A {
    }

    /**
     * Класс C наследует B и использует super для доступа к полю и методу A.
     */
    static class C extends B {

        /**
         * Переопределенный метод класса C.
         */
        void method() {
            // Обращение к полю суперкласса A
            int a = super.a;

            // Вызов метода суперкласса A
            super.method();

            System.out.println("Метод класса C, a = " + a);
        }
    }

    /**
     * Точка входа программы.
     */
    public static void main(String[] args) {
        C objC = new C();
        objC.method();
    }
}
// Пояснение:
    // super.a находит поле a в классе A, так как в классе B его нет.
    // super.method() вызывает метод method() из класса A.
    // Таким образом, super ищет членов по цепочке наследования вверх, пока не найдет первый подходящий.