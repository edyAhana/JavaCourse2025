/**
 * Пользовательское исключение, демонстрирующее базовую структуру
 * собственных классов исключений в Java.
 *
 * Обычно в классе исключения определяют как минимум три конструктора:
 *  1) конструктор без параметров;
 *  2) конструктор, принимающий строку сообщения;
 *  3) конструктор, принимающий сообщение и причину (cause).
 */
public class MyFirstException extends Exception {

    /**
     * Конструктор без параметров.
     * Создаёт исключение без подробного сообщения.
     */
    public MyFirstException() {
        super();
    }

    /**
     * Конструктор, принимающий строку сообщения.
     *
     * @param message текстовое описание ошибки
     */
    public MyFirstException(String message) {
        super(message);
    }

    /**
     * Конструктор, принимающий сообщение и причину исключения.
     *
     * @param message текст ошибки
     * @param cause исходное исключение, вызвавшее данную ошибку
     */
    public MyFirstException(String message, Throwable cause) {
        super(message, cause);
    }
}
