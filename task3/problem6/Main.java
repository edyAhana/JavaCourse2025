public class Main {
    public static void main(String[] args) {
        int i = 130;
        byte b = (byte) i;

        double d = 9.78;
        int j = (int) d;

        long l = 1000L;
        short s = (short) l;

        float f = 5.67f;
        byte c = (byte) f;

        System.out.println(b);
        System.out.println(j);
        System.out.println(s);
        System.out.println(c);
    }
}
